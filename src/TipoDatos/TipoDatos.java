package TipoDatos;

public class TipoDatos {
    public static void main(String[] args) {
        //Tipos de datos en Java

        short a = 1;
        System.out.println(a);

        byte b = 5;
        System.out.println(b);

        int c = 500000;
        System.out.println(c);

        long d = 10000000;
        System.out.println(d);

        float e = 3.1415f;
        System.out.println(e);

        double f = 3.14159248;
        System.out.println(f);

        char g = 'A';
        System.out.println(g);

        boolean h = true;
        System.out.println(h);

        //ENVOLTORIOS

        Integer entero1 = 9;
        Float bb = 3.14_15f;
        System.out.println(bb);
        System.out.println(entero1.intValue());

        //Estructurados  CAmbia caracteristicas a los datos

        String nombre = "JUANIGNACIO";
        System.out.println(nombre);
        System.out.println(nombre.toLowerCase());

        //Constante  ==**Van en mayusculas para facil identificacion

    }
}